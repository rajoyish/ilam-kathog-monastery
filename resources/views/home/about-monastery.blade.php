                <p>निर्माणको यो लगभग ८ वर्षको अवधीसम्म आईपुग्दा यस गुम्बाको ९५% निर्माण कार्य सम्पन्न भईसकेको छ ।
                    गाउँ
                    समाज र विभिन्न ठाउँका दानदाताहरूको चन्दा सहयोग सङ्कलनबाट हाल सम्म कुल लागत १६,००,०००/- को खर्चमा
                    पुन: निर्माण कार्य सम्पन्न हुँदै गरेको यस गुम्बा यस क्षेत्रमा बसोबास गर्नुहुने सिङ्गो बौद्ध समाज
                    लगायत सबै जाति र धर्ममा आस्था राख्नुहुने यहाँका सिङ्गो मानव समाज माझमा सामाजिक एवम् धार्मिक
                    विकास
                    केन्द्रको रूपमा स्थापना भई भौतिक एवम् आध्यत्मिक शिक्षा आर्जनको एउटा उत्कृष्ट श्रोत बनेको छ ।</p>
                <p>हाम्रो देश नेपालमा जन्मलिनुभएको शान्तनायक भगवान् बुद्धद्वारा प्रतिपादित बौद्ध दर्शन त्रिशिक्षा
                    शील,
                    समाधी र प्रज्ञाको अध्ययन र अभ्यास गरी मानव जीवनलाई सत्कर्ममा लगाएर नैतिकवान् बनाउँदै भौतिक एवम्
                    आध्यत्मिक सुख र शक्ति प्राप्त गराउने एउटा पवित्र अभ्यास केन्द्रको रूपमा स्थापना भएको छ । जसले
                    हाम्रो
                    सिङ्गो समाजलाई सभ्य मार्गमा डोहोर्याएर धार्मिक, सामाजिक अनि जानीय, संस्कार, संस्कृति आपसी
                    सद्भावको
                    विकास सँगैसँगै विशेषत यस क्षेत्रलाई धार्मिक पर्यटकीय नमूना गाउँ बनाउनुमा पनि यस गुम्बाको अत्यन्त
                    महत्वपूर्ण भूमिका रहेको छ ।</p>
                <p>बौद्ध दर्शन प्रबर्द्धन तथा गुम्बा विकास समिति स्थानीय विकास मन्त्रालय नेपाल सरकारको अभिलेख
                    न.९९/०५६६१९ मा वि.सं. ......मा प्रमुख लामा गणेश गुरूङ (धर्मनाम साङ्गे योन्तेन गुरूङ)को नाममा
                    सार्वजानिक गुम्बाको रूपमा दर्ता भैसकेको छ । साथै यस गुम्बा जि.प्र.का. इलाम को जि.प्र. द.न. ८३८
                    मा
                    पनि वि.स.२०६९/०४/०५ गते दर्ता भई पूर्णरूपले वैधानिक बनिसकेको छ ।</p>
                <p>गुम्बा व्यवस्थापन समिति र समाजको बृहत उपस्थितीमा नियमित वार्षिक साधरण सभाले पास गरी हरेक वर्ष यस
                    गुम्बाको आयव्यय लेखापरीक्षण वार्षिक नविकरण जि.प्र का.मा पेस गर्दै अत्यन्त पारदर्शी ढङ्गले गुम्बा
                    सञ्चालन हुँदै आइरहेको छ ।</p>
                <p>गुम्बा शिलान्यास र गुम्बा निर्माणको क्रममा मिति .... मा यस गुम्बामा काठमाडौँ महा बौद्ध विहार
                    सेचेन
                    गुम्बाका परम पूजनीय वरिष्ठ धर्मगुरू लामा लुङरिग ङिमा रिन्पोक्षेज्यू पाल्नुभई गुम्बाको प्राण
                    प्रतिष्ठान र बृहत विश्व शान्ती वज्रकिलाय पूजा साथै धार्मिक प्रवचन गर्नुभएको थियो ।</p>
                <p>मिति ... मा सेचेन महा बौद्ध विहारबाट पुनः श्रद्धेय गुरू योङजीन खेन्पो येशे ग्याल्छेन रिन्पेक्षे
                    पाल्नुभई त्रिरत्न समुच्चय (कोन्क्षोग चिदुई)को बृहत् अभिषेक(वाङ) प्रदान गर्नुभएको थियो ।</p>
