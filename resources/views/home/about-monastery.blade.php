                <h2 class="leading-relaxed text-center text-red-900">
                    काथोग नमनङ होसेल छोलिङ गुम्बा
                </h2>
                <ul role="list">
                    <li>सन्दकपुर-४ माबु, इलाम, प्रदेश नं. १, पूर्व नेपाल</li>
                </ul>
                <p>वि.स. २०३५ सालमा स्थापना भएर यस क्षेत्रको समस्त बौद्ध धर्मलम्वीहरू माझ विशेष धार्मिक, सामाजिक
                    एवम्
                    सांस्कृतिक सेवामा निरन्तर सञ्चालनमा रही आएको पुरानो गुम्बा काथोग नम्नङ होईसल क्षोलिङ (गुम्बा)
                    बि.स.२०६९ र २०७२ सालको विनाशकारी महाभुकम्पमा परी जसको सम्पूर्ण संरचनाहरू पूर्णरूपमा क्षतिग्रस्त
                    बन्न
                    पुग्यो । भुकम्पको कारण सबैको आस्थाको धरोहर गुम्बा पूर्णरूपले ध्वस्त बनेसँगै अलमल बनेको समाजलाई
                    सम्हाल्न गुम्बालाई तत्कालै अन्तै स्थानान्तरण गरी पुन:निर्माणको आवश्यक रहेको समाजलाई महसुस भयो ।
                </p>
                <blockquote>
                    <strong>
                        उक्त नयाँ गुम्बा निर्माणको निमित्त पर्याप्त जग्गा पनि नभईरहेको कठिन त्यो समयमा यसै गाउँमा
                        जन्म भई
                        बेलायत र अमेरिकामा रहनुहुने पुण्यवान् जग्गा दाताहरू स्व. रमेश गुरूङ, उमेश गुरूङ र गणेश
                        गुरूङद्वारा ५
                        रोपनी जग्गा खरिद गर्नुभई नव गुम्बा निर्माणको निमित्त जग्गा समाजलाई अर्पण गर्नुभयो ।
                    </strong>
                </blockquote>
                <h2 class="leading-relaxed">
                    पूजनीय गुरू लामा लुङरिग ङिमा रिन्पोक्षेज्यूको बाहुलीबाट बृहत् रूपमा गुम्बा सिलान्यास
                </h2>
                <p>गुम्बा निर्माणको निमित्त जग्गा उपलब्ध भएसँगै यसै सन्दकपुर-४, माबु निवासी स्थानीय वरिष्ठ धर्मगुरू
                    गुम्बाका प्रमुख गुरू (वज्राचार्य) लामा गणेश गुरूङको अध्यक्षतामा सहयोगी समाजसेवी यस क्षेत्रको सबै
                    धर्मगुरूहरु, स्थानीय विद्वान् वर्ग, विशिष्ठ व्यक्तित्वहरू, सामाजिक एवम् धार्मिक क्षेत्रमा आवद्ध
                    अन्य
                    समाजसेवी मान्यजनहरू र विशेषगरी यस क्षेत्रमा बसोबास गर्नुहुने लगभग ४०० भन्दा बढी बौद्ध धर्मालम्बी
                    गुरूङ परिवार र अन्य विभिन्न जातजाति, भिन्न-भिन्न धर्ममा आस्थावान् श्रद्धालु भक्तजनहरू, गाउँका
                    अगुवा
                    र देशविदेशमा रहनुहुने दानवीर दाताहरू सबैको सहयोग समर्थन अनि सल्लाह सुझावहरूअनुरूप यस गुम्बा पुनः
                    वि.
                    सं. २०७२ सालमा उक्त नयाँ ठाउँमा स्थानान्तरण गरी काठमाडौँ सेचेन महा बौद्ध विद्यालयको पूजनीय गुरू
                    लामा
                    लुङरिग ङिमा रिन्पोक्षेज्यूको बाहुलीबाट बृहत् रूपमा गुम्बा सिलान्याससँगै निर्माण कार्य प्रारम्भ
                    भयो ।
                </p>
