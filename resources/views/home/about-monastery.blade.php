                    गुम्बाका परम पूजनीय वरिष्ठ धर्मगुरू लामा लुङरिग ङिमा रिन्पोक्षेज्यू पाल्नुभई गुम्बाको प्राण
                    प्रतिष्ठान र बृहत् विश्व शान्ति वज्रकिलाय पूजा साथै धार्मिक प्रवचन गर्नुभएको थियो ।
                </p>
