                <p>Lama Ganesh Gurung, also known as Sangay Yhonten, hails from Sandakpur-4 in Mabu Ilam. He has
                    dedicated approximately sixteen years of his life to the service and teaching at the Kathog Namnang
                    Hoesal Chhyoling Monastery.</p>
                <p>His journey into Buddhist philosophy began in 1985, spanning six years of study at Boudha Arghau
                    Sadan, situated in Ramghaat, Pokhara, Nepal.</p>
                <p>Following this foundational education, Lama Ganesh Gurung embarked on a more profound spiritual path
                    under the benevolent guidance of the esteemed Guru H.H. Chatral Sangay Dorje Rinpoche, a luminary of
                    the Ngingma lineage. As a devoted disciple of His Holiness, he delved into the profound teachings of
                    Mahayana Buddhism, receiving numerous empowerments (Wang), transmissions (Lung), and teachings (Thi)
                    from his revered master.</p>
