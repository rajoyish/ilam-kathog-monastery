                <p>Subsequently, he immersed himself in traditional meditation for a duration of three years and three
                    months. He undertook this practice at Kunkhap Yoesal Thonkdol Choeling in Dakshinkali V.D.C. Ward-8,
                    Pharping Phulchok, Kathmandu, Nepal, from 1991 to 1997, and later at Dupdey Chenpo Ngedon Namgyaling
                    Monastery and Retreat Center in Mabu-5, Ilam, Nepal, from 2001 to 2005. During this intensive
                    retreat, he completed the preliminary practices (Ngondro) and mastered various meditation techniques
                    encompassing the development and completion stages of the Longchen Ngingthik and Konchok Chidu.</p>
                <p>Upon successfully concluding his rigorous retreat, Lama Ganesh Gurung emerged as a capable Vajra
                    Master, ready to serve his community and society at large by disseminating Buddhist teachings and
                    participating in religious rituals and prayers.</p>
                <p>In addition to his spiritual mentor, H.H. Chatral Sangey Dorje Rinpoche, Lama Ganesh Gurung received
                    empowerments and transmissions from a constellation of esteemed masters, including H.H. Penor
                    Rinpoche, H.H. Thulshik Rinpoche, H.H. Dodrupchen Rinpoche, H.H. Yangthang Rinpoche, H.H. Mogchha
                    Rinpoche, H.H. Dudjyo Yangsi Rinpoche, and H.H. Shechen Rabjam Rinpoche.</p>
                <p>At present, Lama Ganesh Gurung (Sangay Yhonten) holds the esteemed position of Chief Lama and
                    Chairperson at Kathog Namnang Hoesal Chhyoling Monastery in his village. Additionally, he serves as
                    the Senior Vice-President of the Gurung (Tamu) Lama Buddhist Association Central Committee in
                    Kathmandu, Nepal. His devotion extends to his role as the Resident Lama of the Nepal Boudha Gumba in
                    Dharan-18, Nepal, and his service to the Ilam Gurung Society in Kathmandu, Nepal.</p>
